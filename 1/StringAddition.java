public class StringAddition {
    public static void main(String[] args) {
        String num1 = "15";
        String num2 = "27";

        int number1 = Integer.parseInt(num1);
        int number2 = Integer.parseInt(num2);

        int sum = number1 + number2;
        System.out.println("The sum is: " + sum);
    }
}
